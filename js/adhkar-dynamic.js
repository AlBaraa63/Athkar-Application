/**
 * Check if current time is morning.
 * @returns {boolean}
 */
function isMorning() {
  const hour = new Date().getHours();
  return hour >= 4 && hour < 12;
}

/**
 * Check if current time is evening.
 * @returns {boolean}
 */
function isEvening() {
  const hour = new Date().getHours();
  return hour >= 12 && hour < 24;
}

